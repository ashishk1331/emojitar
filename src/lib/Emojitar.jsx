function colorValue(color){
	let ref = {}
	'0123456789'.split('').map(alpha => ref[alpha] = parseInt(alpha))
	'abcdef'.split('').map((alpha, ind) => ref[alpha] = 10 + ind)
	return color.substring(1).toLowerCase().split('').reduce((prev, cur) => prev + ref[cur], 0)
}

